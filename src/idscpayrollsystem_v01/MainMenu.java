    public ArrayList<User> userList(){
        ArrayList<User> usersList = new ArrayList<>();
            try {
                String sql = " select * FROM employee_information";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                User user;
                //String fname = "first_name" + "surname";
                while(rs.next()){
                    user = new User(
                            rs.getInt("emp_id"),
                            rs.getString("fullname"),
                            rs.getString("email"), 
                            rs.getString("department"), 
                            rs.getString("status"), 
                            rs.getString("contact"), 
                            rs.getString("emergency_con"), 
                            rs.getString("contact_prsn"), 
                            rs.getString("address"), 
                            rs.getString("house_no"), 
                            rs.getString("post_code"), 
                            rs.getString("bank_name"), 
                            rs.getString("acc_no"), 
                            rs.getString("bank_branch"),
                            rs.getString("blood_group"), 
                            rs.getString("marital_stat"),
                            rs.getDate("dob"),
                            rs.getDate("date_hired"));
                    usersList.add(user);
                }
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            return usersList;
    }
    
    public ArrayList<Dept> deptList(){
        ArrayList<Dept> deptsList = new ArrayList<>();
            try {
                    String sql = " select * FROM department_information";
                    Statement st = conn.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    Dept dept;
                    while (rs.next()){
                        dept = new Dept(
                                rs.getString("department"),
                                rs.getInt("monthlyRate"),
                                rs.getInt("daysperMonth"),
                                rs.getInt("dailyRate"),
                                rs.getInt("hoursperDay"),
                                rs.getInt("hourlyRate"),
                                rs.getString("shift"));
                        deptsList.add(dept);
                    }
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        return deptsList;
    }
    
    public void show_dept(){
        ArrayList<Dept> list = deptList();
        DefaultTableModel model = (DefaultTableModel)tbl_departmentinfo.getModel();
        Object[] row = new Object[7];
        for(int i = 0; i < list.size(); i++){
            row[0] = list.get(i).getdept();
            row[1] = list.get(i).getmRate();
            row[2] = list.get(i).getdpM();
            row[3] = list.get(i).getdRate();
            row[4] = list.get(i).gethpD();
            row[5] = list.get(i).gethRate();
            row[6] = list.get(i).getshift();
            model.addRow(row);
        }
    }
    
    public void show_user(){
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)tbl_employeeinfo.getModel();
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++){
            row[0] = list.get(i).getempid();
            row[1] = list.get(i).getname();
            row[2] = list.get(i).getemail();
            row[3] = list.get(i).getdept();
            row[4] = list.get(i).getstatus();
            model.addRow(row);
        }
    }
    
